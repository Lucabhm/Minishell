size_t	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*freeup(char *buffer)
{
	if (buffer)
		free(buffer);
	return (NULL);
}
